# 字串運算
s = "Hello"
print(s)
s = "Hello"+"world"
print(s)
s = "Hello\nWorld"  # 換行
print(s)  # 換行
s = """Hello  

World"""
print(s)
s = "Hello"*3+"World"
print(s)

# 字串會對內部的字元編號(索引)，從0開始算起
s = "Hello"
print(s[4])  # 會得到字母o
print(s[2])  # 會得到字母L
print(s[1:4])  # 包含開頭編號不包含結尾編號
print(s[1:])  # 開頭算起到全部
print(s[:4])  # 開頭算起不包含結尾
